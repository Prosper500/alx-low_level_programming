#include "main.h"

/**
 * *_strcat - A function that link two string
 * @dest: A string variable
 * @src: Another string variable
 * Return: character
 */

char *_strcat(char *dest, char *src)
{
	int i = 0;
	int j = 0;

	while (dest[i] != '\0')
	{
		i++;
	}
	while (src[j] != '\0')
	{
		dest[i] = src[j];
		i++;
		j++;
	}
	dest[i] = '\0';
	return (dest);
}
